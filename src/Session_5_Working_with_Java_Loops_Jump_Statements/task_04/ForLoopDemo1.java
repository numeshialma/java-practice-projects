package Session_5_Working_with_Java_Loops_Jump_Statements.task_04;

public class ForLoopDemo1 {

    public static void main(String[] args) {

        // Example 2: 1....10 even numbers
        for(int i=1; i<=10; i++){
            if(i%2==0){
                System.out.println(i);
            }
        }
    }
}
