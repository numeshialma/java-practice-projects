package Session_5_Working_with_Java_Loops_Jump_Statements.task_01;

public class WhileLoopDemo5 {

    public static void main(String[] args) {

        // Example 5 :       print 10....1

        int i=10;

        while(i>0){
            System.out.println(i);
            i--;
        }
    }
}
