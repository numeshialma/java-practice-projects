package Session_5_Working_with_Java_Loops_Jump_Statements.task_02;

public class DoWhileLoop {

    public static void main(String[] args) {

        // 1...10 numbers

        int i=1;
        do{
            System.out.println(i); // 1 2 3 4  .. 10
            i++; // 2 3 4 5 ... 11
        }while(i<=10);
    }
}

