package Session_2_Working_with_Java_Variables_and_Data_Types.task_04;

public class VariablesDemo {

    public static void main(String[] args) {

        // Approach 01 - if all the variables belongs to different data types
        int a = 100;
        int b = 200;
        int c = 300;

        // Approach 02 - if all the variables are belongs to same data type
        int d,e,f;
        d = 100;
        e = 200;
        f = 300;

        // Approach 03 -  if all the variables are belongs to same data type
        int g = 100, h = 200, i = 300;

        System.out.println(g);
        System.out.println(h);
        System.out.println(i);

    }
}
