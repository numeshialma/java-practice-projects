package Session_4_Working_with_Java_Conditional_Statements.task_02;

public class ifConditionDemo1 {
    public static void main(String[] args) {

        int person_age = 15;

        if(person_age>=18)
            System.out.println("eligible for vote");

        else
            System.out.println("not eligible for vote");

    }

    /*
    for single statement we can use this without brackets.
    But for multiple statements , we should use brackets
     */

}
