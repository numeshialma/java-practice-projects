package Session_8_Working_with_Java_Strings_String_Methods.task_01;

public class Main1 {

    public static void main(String[] args) {

        String s = "welcome";
        int l = s.length();
        System.out.println(l);   // output = 7

        String s1 = "welcome";
        s1.length();
        System.out.println(s1.length());    // output = 7

        System.out.println("welcome".length());    // output = 7
    }


}
